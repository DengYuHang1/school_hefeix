#include <stdio.h>

int main(void)
{
	char a[80];
	int b;
	
	gets(a);
	
	for(b=0;a[b]!='\0';b++)
	{
		if(a[b]>='a'&&a[b]<='z')
		{
			a[b]-=32;
		}
		else if(a[b]>='A'&&a[b]<='Z')
		{
			a[b]+=32;
		}
	}
	
	puts(a);
	return 0;
}
